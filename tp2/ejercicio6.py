# Ingresar 3 numeros enteros, calcular su promedio y mostrarlo por pantalla
num1 = int(input("Ingrese numero 1: "))
num2 = int(input("Ingrese numero 2: "))
num3 = int(input("Ingrese numero 3: "))
suma = num1 + num2 + num3
promedio = suma / 3
print("El promedio de los 3 numeros es : ",promedio)